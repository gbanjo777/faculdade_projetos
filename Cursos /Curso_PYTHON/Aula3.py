"""
    Docstring também é string
    Python = Linguagem de programação
    Tipo de tipagem = Dinâmica / Forte
    str -> string -> texto
    Strings são textos que estão dentro de aspas
    """
print(1234)          # Inteiro
print(12.34)        # Ponto flutuante  # Float
print('Olá, Mundo!') # String com aspas simples
print("Olá, Mundo!") # String com aspas duplas 
    # Escapes
print("Olá,\nMundo!") # Quebra de linha
print("Olá,\tMundo!") # Tabulação / Espaçamento
print("Olá, \"Mundo\"!") # Aspas duplas dentro de aspas
print('Olá, \'Mundo\'!') # Aspas simples dentro de aspas
    # r antes da string ignora escapes
print(r"Olá,\r Mundo!")  # Raw string
print(2, "Luiz 'Otávio'") # Vírgula adiciona espaço automático