# Tipo de dado bool (boolean)
# Ao se questionar algo em um programa,
#Só existem duas respostas possíveis: Verdadeiro(True) ou Falso(False)
# Dentre eles, o ==, que é um operador lógico que questiona se um 
# valor é igual a outro.
print(10 == 10)
print(10 == 5)
print(type(10 == 11)) #Nesse caso, o resultado é um bool, pois os números entre parenteses são boolianos